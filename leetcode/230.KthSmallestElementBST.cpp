struct TreeNode
{
    int val;
    TreeNode *left;
    TreeNode *right;
    TreeNode() : val(0), left(nullptr), right(nullptr) {}
    TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
    TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
};

class Solution
{
public:
    int kthSmallest(TreeNode *root, int k)
    {
        stack<TreeNode *> tracker;

        while (root != nullptr || !tracker.empty())
        {
            while (root != nullptr)
            {
                tracker.push(root);
                root = root->left;
            }

            root = tracker.top();
            tracker.pop();

            if (--k == 0)
            {
                return root->val;
            }

            root = root->right;
        }

        return 1;
    }
};

